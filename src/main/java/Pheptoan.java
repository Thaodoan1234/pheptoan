public class Pheptoan {
    public static void main(String[] args) {
        float a = 7F;
        float b = 6F;
        float tong,hieu,tich,thuong;

        tong = a + b;
        hieu = a - b;
        tich = a * b;
        thuong = a/b;

        System.out.println("Số thứ nhất = " + a);
        System.out.println("Số thứ hai = " + b);
        System.out.println("Tổng hai số = " + tong);
        System.out.println("Hiệu hai số = " + hieu);
        System.out.println("Tích hai số = " + tich);
        System.out.println("Thương của hai số = " + thuong);
    }
}
